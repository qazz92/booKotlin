public class AttendListViewItem {
    private String attend_id;
    private String attend_name;
    private String attend_on;

    public String getAttend_id() {
        return attend_id;
    }

    public void setAttend_id(String attend_id) {
        this.attend_id = attend_id;
    }

    public String getAttend_name() {
        return attend_name;
    }

    public void setAttend_name(String attend_name) {
        this.attend_name = attend_name;
    }

    public String getAttend_on() {
        return attend_on;
    }

    public void setAttend_on(String attend_on) {
        this.attend_on = attend_on;
    }
}