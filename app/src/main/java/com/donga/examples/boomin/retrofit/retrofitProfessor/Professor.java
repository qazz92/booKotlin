public class Professor {
    String id, name, room, tel, email, major, pos;

    public String getEmail() {
        return email;
    }

    public String getId() {
        return id;
    }

    public String getMajor() {
        return major;
    }

    public String getName() {
        return name;
    }

    public String getPos() {
        return pos;
    }

    public String getRoom() {
        return room;
    }

    public String getTel() {
        return tel;
    }
}
